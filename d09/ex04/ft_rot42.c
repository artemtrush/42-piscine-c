char	*ft_rot42(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (((str[i] >= 'a') && (str[i] <= 'j'))
				|| ((str[i] >= 'A') && (str[i] <= 'J')))
			str[i] = str[i] + 16;
		else if ((str[i] > 'j') && (str[i] <= 'z'))
			str[i] = 'a' + (str[i] - 'z') + 15;
		else if ((str[i] > 'J') && (str[i] <= 'Z'))
			str[i] = 'A' + (str[i] - 'Z') + 15;
		i++;
	}
	return (str);
}
