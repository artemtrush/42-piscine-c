int	ft_atoi(char *str)
{
	int v;
	int n;
	int i;

	n = 0;
	i = 0;
	v = 0;
	while ((str[i] == ' ') || (str[i] == '\t') || (str[i] == '\n')
			|| (str[i] == '\v') || (str[i] == '\f') || (str[i] == '\r'))
		i++;
	if (str[i] == '-')
		n = 1;
	if (str[i] == '+' || str[i] == '-')
		i++;
	while (str[i] >= 48 && str[i] <= 57)
	{
		v = v * 10;
		v = (str[i] - 48) + v;
		i++;
	}
	if (n == 1)
		return (-v);
	else
		return (v);
}
