int	ft_atoi(char *str)
{
	int		num;
	char	sign;

	num = -1;
	while (*str != '\0')
	{
		if ((*str == 45 || *str == 43) && num == -1)
			sign = *str;
		if (*str > 47 && *str < 58)
		{
			if (num == -1)
				num = (*str - '0');
			else
				num = num * 10 + (*str - '0');
		}
		str = str + 1;
	}
	if (sign == 45)
		num = num * (-1);
	return (num);
}
