int		ft_putchar(char c);

void	ft_putnbr(int nb)
{
	int x[3];

	if (nb < 0)
	{
		nb = -nb;
		ft_putchar(45);
	}
	while (nb > 9)
	{
		x[0] = 0;
		x[1] = nb;
		while (x[1] > 9)
		{
			x[1] = x[1] / 10;
			x[0]++;
		}
		ft_putchar(x[1] + 48);
		x[2] = x[0];
		while (x[2]-- > 0)
		{
			x[1] = x[1] * 10;
		}
		nb = nb - x[1];
	}
	ft_putchar(nb + 48);
}
