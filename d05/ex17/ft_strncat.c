char	*ft_strncat(char *dest, char *src, int nb)
{
	int i;
	int t;

	t = 0;
	i = 0;
	while (dest[i])
		i++;
	while (src[t] && nb > 0)
	{
		dest[i] = src[t];
		t++;
		i++;
		nb--;
	}
	dest[i] = '\0';
	return (dest);
}
